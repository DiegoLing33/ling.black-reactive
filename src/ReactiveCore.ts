import Watcher from "./Watcher";

/**
 * Reactive core
 */
export class ReactiveCore<T> extends Watcher<T>{
    protected __value: T;

    /**
     * Returns the value
     */
    public get value(): Readonly<T>{
        return this.__value;
    }

    public constructor(initialValue: T) {
        super();
        this.__value = initialValue;
    }

    /**
     * Shakes the watchers to call it
     */
    public shake(){
        this.__watchers.forEach(value => value(this.value));
    }
}
